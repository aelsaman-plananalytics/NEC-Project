"""
Match result model for NEC Engineering Analysis System.

Represents a matching result between a scope item and an activity item,
including scores from different matching engines.
"""

from sqlalchemy import Column, String, ForeignKey, Float, DateTime
from sqlalchemy.orm import relationship
from sqlalchemy.sql import func
import uuid

from app.database import Base


class MatchResult(Base):
    """
    Match result model representing a match between a scope item and an activity.
    
    Match results are generated by the multi-signal engineering pipeline,
    combining scores from rule-based matching, embedding similarity, and
    LLM validation. The final score is computed using weighted ensemble scoring.
    """
    
    __tablename__ = "match_results"
    
    # Primary key
    id = Column(
        String,
        primary_key=True,
        default=lambda: str(uuid.uuid4()),
        nullable=False,
        comment="Unique match result identifier (UUID as string)"
    )
    
    # Foreign keys
    project_id = Column(
        String,
        ForeignKey("projects.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
        comment="Reference to the parent project"
    )
    
    scope_id = Column(
        String,
        ForeignKey("scope_items.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
        comment="Reference to the matched scope item"
    )
    
    activity_id = Column(
        String,
        ForeignKey("activities.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
        comment="Reference to the matched activity"
    )
    
    # Matching scores
    rule_score = Column(
        Float,
        nullable=True,
        comment="Score from rule-based matching engine (0.0 to 1.0)"
    )
    
    embedding_score = Column(
        Float,
        nullable=True,
        comment="Score from embedding similarity matching (0.0 to 1.0)"
    )
    
    llm_score = Column(
        Float,
        nullable=True,
        comment="Score from LLM validation (0.0 to 1.0)"
    )
    
    final_score = Column(
        Float,
        nullable=False,
        comment="Final weighted ensemble score (0.0 to 1.0)"
    )
    
    status = Column(
        String(50),
        nullable=False,
        default="unsure",
        comment="Match status: matched, unsure, or unmatched"
    )
    
    # Timestamps
    created_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        comment="Match result creation timestamp"
    )
    
    # Relationships
    project = relationship(
        "Project",
        back_populates="match_results"
    )
    
    scope_item = relationship(
        "ScopeItem",
        back_populates="match_results"
    )
    
    activity_item = relationship(
        "ActivityItem",
        back_populates="match_results"
    )
    
    def __repr__(self) -> str:
        """String representation of the match result."""
        return (
            f"<MatchResult(id={self.id}, scope_id={self.scope_id}, "
            f"activity_id={self.activity_id}, final_score={self.final_score:.3f}, "
            f"status='{self.status}')>"
        )

